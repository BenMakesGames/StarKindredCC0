namespace StarKindred.Common.Entities;

public enum Nature
{
    Cavalier, // willpower when fighting a Giant
    Competitor, // willpower when the highest-level in a mission with at least one other
    Defender, // willpower after succeeding in a monster-hunt
    Explorer, // willpower when treasure-hunting
    Evangelist, // willpower after recruiting
    Loner, // willpower after completing a mission solo
    Monger, // willpower after collecting gold
    Perfectionist, // willpower after getting great success
    ThrillSeeker, // willpower when participating in a mission with 50% or worse odds
    Visionary, // willpower when seeing the Oracle
}